def sort_array_fastest(arr):
  """
  Сортирует массив чисел по месту, используя самый быстрый алгоритм, доступный в Python.

  Args:
    arr: Список чисел для сортировки.
  """
  arr.sort()

# Пример использования
my_array = [4, 2, 5, 1, 3]
sort_array_fastest(my_array)
print(my_array)  # Output: [1, 2, 3, 4, 5]
